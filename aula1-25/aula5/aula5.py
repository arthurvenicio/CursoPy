"""
+ - Addition
- - Subtraction
* - Multiplication
/ - Division
// - Floor division
** - Exponentiation
% - Modulus
()
"""

print(10 + 10)   # Addition
print(20 - 10)   # Subtraction
print(10 * 10)   # Multiplication
print(10 / 10)   # Division
print(14 // 2)   # Floor division
print(10 ** 10)  # Exponentiation
print('Aqui' ,3 ** 2)  # Exponentiation
print(7 % 2)     # Modulus
print(7 + (7*2))


print(10 * '10')  # Repetition of strings
print('10' + '10')  # String Concatenation